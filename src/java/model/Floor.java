public class Floor {
    private int number;
    private int use;
    private int notuse;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getUse() {
        return use;
    }

    public void setUse(int use) {
        this.use = use;
    }

    public int getNotuse() {
        return notuse;
    }

    public void setNotuse(int notuse) {
        this.notuse = notuse;
    }
    
    
}
